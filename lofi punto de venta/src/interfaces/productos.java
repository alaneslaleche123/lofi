    private void guardarPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarPActionPerformed
        conectar con = new conectar();
        Connection cn = null;
        PreparedStatement psPaquete = null;
        PreparedStatement psDetalle = null;
        ResultSet rs = null;

        try {
            cn = con.getConnection();
            cn.setAutoCommit(false);

            // --- Obtener IVA ---
            ComboItem itemIva = (ComboItem) ivacombox.getSelectedItem();
            int idIvaSeleccionado = (itemIva != null) ? itemIva.getId() : 1;

            // --- Obtener categoría ---
            ComboItem itemCategoria = (ComboItem) departamentocombox.getSelectedItem();
            int idCategoriaSeleccionada = (itemCategoria != null) ? itemCategoria.getId() : 1;

            // --- Validar campos ---
            String nombre = nombre_paquete.getText().trim();
            String descripcion = descripcionp.getText().trim();
            String precioTexto = precio_paquete.getText().trim();

            if (nombre.isEmpty() || precioTexto.isEmpty()) {
                JOptionPane.showMessageDialog(this, "⚠️ Ingresa nombre y precio del paquete.");
                return;
            }

            double precio = Double.parseDouble(precioTexto);

            // --- Validar selección de productos ---
            int[] filasSeleccionadas = tablaPaquetesMuestra.getSelectedRows();
            if (filasSeleccionadas.length == 0) {
                JOptionPane.showMessageDialog(this, "⚠️ Selecciona al menos un producto para agregar al paquete.");
                return;
            }

            // 1️⃣ Insertar el paquete principal
            String sqlPaquete = "INSERT INTO paquetes (nombre_paquete, descripcion, precio_paquete, id_categoria, id_iva) "
            + "VALUES (?, ?, ?, ?, ?)";
            psPaquete = cn.prepareStatement(sqlPaquete, Statement.RETURN_GENERATED_KEYS);
            psPaquete.setString(1, nombre);
            psPaquete.setString(2, descripcion);
            psPaquete.setDouble(3, precio);
            psPaquete.setInt(4, idCategoriaSeleccionada);
            psPaquete.setInt(5, idIvaSeleccionado);
            psPaquete.executeUpdate();

            rs = psPaquete.getGeneratedKeys();
            int idPaquete = 0;
            if (rs.next()) {
                idPaquete = rs.getInt(1);
            }

            // 2️⃣ Insertar los productos seleccionados en detalle_paquete
            String sqlDetalle = "INSERT INTO detalle_paquete (id_paquete, id_producto, cantidad_producto) VALUES (?, ?, ?)";
            psDetalle = cn.prepareStatement(sqlDetalle);

            for (int fila : filasSeleccionadas) {
                int idProducto = Integer.parseInt(tablaPaquetesMuestra.getValueAt(fila, 0).toString());

                // puedes pedir cantidad en un JTextField (por ahora fija en 1)
                int cantidadProducto = 1;

                psDetalle.setInt(1, idPaquete);
                psDetalle.setInt(2, idProducto);
                psDetalle.setInt(3, cantidadProducto);
                psDetalle.addBatch();
            }
            psDetalle.executeBatch();

            // 3️⃣ Confirmar transacción
            cn.commit();

            JOptionPane.showMessageDialog(this, "✅ Paquete creado con " + filasSeleccionadas.length + " productos.");
            mostrarPaquetesConProductos();

            // Limpiar campos
            nombre_paquete.setText("");
            descripcionp.setText("");
            precio_paquete.setText("");

            mostrarPaquetesEnTabla();

        } catch (Exception e) {
            try {
                if (cn != null) {
                    cn.rollback();
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "⚠️ Error al revertir transacción: " + ex.getMessage());
            }
            JOptionPane.showMessageDialog(this, "⚠️ Error al guardar paquete: " + e.getMessage());
            e.printStackTrace();
        } finally {
            try {
                if (rs != null) {
                    rs.close();
                }
                if (psPaquete != null) {
                    psPaquete.close();
                }
                if (psDetalle != null) {
                    psDetalle.close();
                }
                if (cn != null) {
                    cn.setAutoCommit(true);
                    cn.close();
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_guardarPActionPerformed

