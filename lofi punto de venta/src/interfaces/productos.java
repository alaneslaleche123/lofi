        tablaPaquetes.setBackground(new java.awt.Color(250, 250, 255));
        tablaPaquetes.setSelectionBackground(new java.awt.Color(60, 130, 210));
        tablaPaquetes.setSelectionForeground(java.awt.Color.WHITE);

